	<h2>Artificial Intelligence in Science Fiction</h2>
	<p class="one">Science fiction has built up the idea of artificial intelligence for years. Giving machines intelligence often spells the end of mankind as they then rise up against their creators and violently free themselves from oppression. There are cautionary tales such as in the film "Spider Man 2" in which the robotic arms of Dr. Octavius start controlling his brain thus forcing him to perform actions of benefit to them. I've never really had an interest in artificial intelligence until recently when I started studying the very basics at university. The only AI Science Fiction that's ever actually interested me was 'Blade Runner' and its novel, 'Do Androids Dream of Electric Sheep?' written by Philip K. Dick. The subtext of the story deals with AI becoming self-aware but still unaware of its true nature, a concept that interested me greatly. Real AI is quite fascinating and in many ways entirely different to what I expected.</p>

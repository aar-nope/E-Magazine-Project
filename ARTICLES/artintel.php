	<p>To start with, what exactly is AI? <a href="http://www.dictionary.com">http://www.dictionary.com</a>  defines it as follows:
	<ol class="def">
	<li>The ability of a computer or other machine to perform those activities that are normally thought to require intelligence.</li>
	<li>The branch of computer science concerned with the development of machines having this ability.</li>
	</ol></p>
	
	<p>If you take a look at this you may notice that the field of AI is actually far broader than many people give it credit for. Even the Google search engine can be said to incorporate some of the research done in AI. The definition of AI and intelligence itself sometimes varies but it's probably safe to say that a simple explanation is that if a machine exhibits a measure of intelligence in some area it can be called AI.</p>

	<p>The AI of movies is usually entirely self aware, emotive, and for all intents and purposes 'alive'. It is capable of learning, reasoning, explaining and deceiving. The AI I've experienced (and this is very basic, scratching only the surface of the depth of studies into AI) is far more specialized, focusing on just one aspect of a problem that could potentially be much larger. There are various areas of research being carried out into creating the humanoid machines of science fiction, but as yet most are still restricted to the realms of the imagination. One very impressive piece of technology is the Honda ASIMO project. This project has created a humanoid robot with some remarkable features. I'm not entirely sure if there's really a practical use for this (I think they're just trying to justify their funding with the applications mentioned on the website) but it scores points for being one of the coolest uses of technology I've ever seen. You can see some videos of ASIMO in action <a href="http://world.honda.com/HDTV/ASIMO/">here</a>.</p>

	<p>From what I can tell <strong>ASIMO</strong> isn't intended to be a particularly human robot beyond superficial levels. He doesn't exhibit emotion or learn outside of his constraints (he can learn areas and navigate them etc. but you couldn't just sit down and teach him to play the piano without getting in and doing some serious reprogramming). He's not going to be fooling anyone into believing that he's human any time soon.</p>

	<p><strong>The Turing Test</strong> is a measure of Artificial Intelligence that was created by Alan Turing. He stated that the question of whether or not machines could think was too vague, and proposed a test that was more specific. His test, without going into too much detail, involved trying to get a computer to fool people into believing that it was human. The people would type a question into a console and receive a response from the computer on the other side. While there has been much work done on systems trying to pass the Turing test, none of them have succeeded yet. There has also been research done into creating machines that can exhibit emotions. However, we're still a long way off from seeing a machine that can appear to be alive.</p>

	<p>The early excitement over AI led to developers trying to create a generic reasoning problem solver that could search through a mass of knowledge that it has acquired and find solutions to any problem that was thrown at it. Unfortunately this proved almost impossible in practice. Today's AI tends to focus on very specific problems and knowledge areas. Expert Systems are programs that are "Experts" in a specific field and can answer queries related to only that field. Their applications include medical diagnosis, credit card application, and other fields where data is to be analyzed.</p>

	<p>I haven't even begun to explore the true extent of AI in this article, but maybe I've piqued your interest. If you want to look into it further, try reading the Wikipedia (free on-line encyclopedia) entry and maybe downloading something like Prolog to have a go and see what real AI is like. I find it quite interesting because it involves a similar mindset to programming as you attempt to break a problem down enough for a computer to understand it, but of course the actual implementation is very different.</p>

	<p>Here's a fun thing to try. Open a copy of <a href="http://www.alicebot.org/">ALICE</a> start a conversation with a friend on your messenger program of choiceHealth Fitness Articles, feed their responses into ALICE and send the program's responses back. See how long it takes before they either work out what's happening or get annoyed and block you (which has been the most common result of my experimentations). Enjoy your exploration of the world of artificial intelligence.</p>

